重要的標準：<br />
• 沒有基因改造/納米/煙燻/輻射&nbsp;<br />
•至少95％的物理加工農產品必須是有機的<br />
•總產品中至少有20％必須是有機的<br />
•非有機化妝品成份需要獲得批准<br />
•有機成分的百分比按重量計算，包括水和礦物質（一些有機標準如ACO和USDA在計算有機物含量時不包括水和礦物質）。<br />
